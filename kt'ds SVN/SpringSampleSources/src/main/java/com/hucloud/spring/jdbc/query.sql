CREATE TABLE NOTE
(
  NOTE_NUMBER   NUMBER                          NOT NULL,
  NOTE_SUBJECT  VARCHAR2(4000 BYTE)             NOT NULL,
  NOTE_CONTENT  VARCHAR2(4000 BYTE)
)
TABLESPACE SYSTEM
PCTUSED    40
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MAXSIZE          UNLIMITED
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            FREELISTS        1
            FREELIST GROUPS  1
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
MONITORING;


