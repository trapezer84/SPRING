public class EducationBoardHistoryVO {
	
	private String educationBoardHistoryId;
	private String memberId;
	private String accessDate;
	private String educationId;

	public String getEducationBoardHistoryId() {
		return educationBoardHistoryId;
	}
	public void setEducationBoardHistoryId(String educationBoardHistoryId) {
		this.educationBoardHistoryId = educationBoardHistoryId;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getAccessDate() {
		return accessDate;
	}
	public void setAccessDate(String accessDate) {
		this.accessDate = accessDate;
	}
	public String getEducationId() {
		return educationId;
	}
	public void setEducationId(String educationId) {
		this.educationId = educationId;
	}
	
}
