package com.ktds.sems.education.vo;

public class EduClassVO {

	private String educationClassId;
	private String educationId;
	private String memberId;
	private String status;
	
	public String getEducationClassId() {
		return educationClassId;
	}
	public void setEducationClassId(String educationClassId) {
		this.educationClassId = educationClassId;
	}
	public String getEducationId() {
		return educationId;
	}
	public void setEducationId(String educationId) {
		this.educationId = educationId;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
